ll read()
{
    ll x = 0,f = 1;
    char ch = getchar();
    while (ch < '0' || ch > '9')
	{
		if (ch == '-')
		f = -1;
		ch = getchar();
	}
    while (ch >= '0' && ch <= '9')
	{
		x = x * 10 + ch - '0';
		ch = getchar();
	}
    return x * f;
}
int t, n, ans = 0;
vector<int> G[maxn];

void dfs(int u, int d) {
	if (u == 0) ans = max(ans, d);
	for (int i = 0; i < G[u].size(); i++) {
		int e = G[u][i];
		dfs(e, d + 1);
	}
}

