//快速幂求逆元,当a是mod的倍数时无解
ll q_pow(ll n, ll x, ll mod)//快速幂全用ll
{
    ll res = 1;
    while (x)
    {
        if (x & 1)
        {
            res = res * n % mod;
        }
        n = n * n % mod;
        x >>= 1;
    }
    return res % mod;
}


